#Exercise 5: Compute Area of Circle. Write a Python program which accepts the radius of a circle from the user and compute the area.
from math import pi
radius=float(input("Input the radius of the circle: "))#Float because radius can be in decimals, which the float data type accepts. Input, so the user can enter their own value to be stored in variable called 'radius'.
area_of_circle=pi*radius**2 #This is formula for area of the circle inside the variable 'area_of_circle'. To be executed later.
print(f"The area of the circle with radius of {radius} is {area_of_circle}.\n")
